function main() {
    const [,, inputPath, outputPath] = process.argv;
    if (!inputPath || !outputPath) {
        console.error("Использование: node cleanPersonalii_v3.js input.csv output.csv");
        process.exit(1);
    }
    console.log(`📂 Читаю: ${inputPath}`);
    let raw = fs.readFileSync(inputPath, "utf-8");
    if (raw.charCodeAt(0) === 0xfeff) { raw = raw.slice(1); console.log("  BOM убран"); }

    const { fields, data } = parseCSV(raw, ";");
    console.log(`  Колонки (${fields.length}): ${fields.join(" | ")}`);
    console.log(`  Строк данных: ${data.length}`);

    const COL = "Персоналии";
    if (!fields.includes(COL)) {
        console.error(`  Колонка "${COL}" не найдена!`);
        process.exit(1);
    }

    let changed = 0, total = 0;
    const out = data.map(row => {
        if (!row[COL]?.trim()) return row;
        total++;
        const orig = row[COL];
        const cleaned = processCell(orig);
        if (cleaned !== orig) changed++;
        return { ...row, [COL]: cleaned };
    });

    fs.writeFileSync(outputPath, "\uFEFF" + serializeCSV(fields, out, ";"), "utf-8");
    console.log(`\n Готово!`);
    console.log(`  Обработано строк: ${total}`);
    console.log(`  Изменено: ${changed}`);
    console.log(`  Сохранено → ${outputPath}`);
}

main();