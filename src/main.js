// [3] ПРЕДОБРАБОТКА ЯЧЕЙКИ ДО РАЗБИВКИ
// Точки внутри строки, которые разделяют персоналии → запятая
// Например: "богоматерь. пророки давид" → "богоматерь, пророки давид"
// ═══════════════════════════════════════════════════════════════════════════
function preprocess(s) {
    // "слово. Слово" или "слово. слово" (точка между именами) → запятая
    // НО не трогаем сокращения (уже должны быть раскрыты)
    s = s.replace(/([а-яё])\.\s+([а-яёА-ЯЁ])/g, "$1, $2");
    // Двоеточие после группы (напр. "пророки: захария") → запятая + убираем множественное
    s = s.replace(/([а-яё]+):\s+/g, "$1, ");
    // "сщмч.евфимий" → "священномученик евфимий" и все слипшиеся
    // "еп.иерусалимский" → "епископ иерусалимский"
    s = s.replace(/\bеп\.([а-яё])/g, "епископ $1");
    // "прав.симеон" → "праведный симеон"
    s = s.replace(/\bправ\.([а-яё])/g, "праведный $1");
    // "прор.иезекееиль" → "пророк иезекееиль"
    s = s.replace(/\bпрор\.([а-яё])/g, "пророк $1");
    // "прп.феодор" → "преподобный феодор"
    s = s.replace(/\bпрп\.([а-яё])/g, "преподобный $1");
    // "преп.феодор" → "преподобный феодор"
    s = s.replace(/\bпреп\.([а-яё])/g, "преподобный $1");
    // "свт.николай" → "святитель николай"
    s = s.replace(/\bсвт\.([а-яё])/g, "святитель $1");
    // "мч.андрей" → "мученик андрей"
    s = s.replace(/\bмч\.([а-яё])/g, "мученик $1");
    // "вмч.георгий" → "великомученик георгий"
    s = s.replace(/\bвмч\.([а-яё])/g, "великомученик $1");
    // "вмц.екатерина" → "великомученица екатерина"
    s = s.replace(/\bвмц\.([а-яё])/g, "великомученица $1");
    // "св.николай" → "святой николай"
    s = s.replace(/\bсв\.([а-яё])/g, "святой $1");
    return s;
}

