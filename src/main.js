const GENDER_FIX = {
    "великомученик": "великомученица",
    "мученик": "мученица",
    "священномученик": "священномученица",
    "преподобный": "преподобная",
    "благоверный": "благоверная",
    "блаженный": "блаженная",
    "преподобномученик": "преподобномученица",
    "равноапостольный": "равноапостольная",
    "праведный": "праведная",
    "пророк": "пророчица",
    "святитель": "святительница",
    "апостол": "апостол", // не меняем
};

function fixGender(token) {
    for (const [maleChin, femaleChin] of Object.entries(GENDER_FIX)) {
        if (token.toLowerCase().startsWith(maleChin + " ")) {
            const rest = token.slice(maleChin.length + 1);
            const firstName = rest.split(/\s+/)[0].toLowerCase();
            if (FEMALE_NAMES.has(firstName)) {
                return femaleChin + " " + rest;
            }
        }
    }
    return token;
}

