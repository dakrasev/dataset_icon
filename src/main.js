function processCell(rawValue) {
    if (!rawValue || typeof rawValue !== "string") return rawValue;
    const trimmed = rawValue.trim().toLowerCase();
    if (!trimmed || trimmed === "nan") return "";

    // 1) В нижний регистр + мягкий перенос
    let s = rawValue.toLowerCase().replace(/\u00AD/g, "").replace(/\uFEFF/g, "");

    // 2) Предобработка (точки-разделители → запятые, сокращения без пробелов)
    s = preprocess(s);

    // 3) Раскрываем сокращения
    s = expandAbbreviations(s);

    // 4) Разбиваем по запятой и точке с запятой
    let tokens = s.split(/[,;]/).map(t => t.trim()).filter(Boolean);

    // 5) Для каждого токена: cleanJunk → expandAbbreviations → fixTypos
    tokens = tokens.flatMap(token => {
        token = cleanJunk(token);
        if (!token) return [];
        token = expandAbbreviations(token);
        token = fixTypos(token);

        // Если fixTypos вернул строку с запятой — разбиваем
        if (token.includes(",")) {
            return token.split(",")
                .map(t => cleanJunk(expandAbbreviations(fixTypos(t.trim()))))
                .filter(Boolean);
        }
        return [token];
    });

    // 6) Разбиваем "и"-пары
    tokens = tokens.flatMap(t => splitAndToken(t));

    // 7) Убираем эпитеты богоматери
    tokens = tokens.map(cleanBogomater);

    // 8) Нормализация Христа/Богородицы
    tokens = normalizeChristAndMother(tokens);

    // 9) Второй проход: fixTypos + дедуп
    tokens = tokens.flatMap(t => {
        t = cleanJunk(t);
        if (!t) return [];
        t = fixTypos(t);
        if (t.includes(",")) {
            return t.split(",").map(x => cleanJunk(dedupPerson(fixTypos(x.trim())))).filter(Boolean);
        }
        t = dedupPerson(t);
        return [cleanJunk(t)];
    }).filter(Boolean);

    // 10) Согласование рода
    tokens = tokens.map(fixGender);

    // 11) Убираем мусорные токены-одиночки
    tokens = tokens.filter(t => {
        const k = t.toLowerCase().trim();
        return !JUNK_TOKENS.has(k) && t.length > 1;
    });

    // 12) Дедупликация в рамках одной ячейки
    const seen = new Set();
    tokens = tokens.filter(t => {
        const key = t.toLowerCase().trim();
        if (seen.has(key)) return false;
        seen.add(key);
        return true;
    });

    return tokens.map(cleanJunk).filter(Boolean).join(", ");
}

