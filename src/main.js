const BOGOMAT_EPITHETS = [
    "великая панагия","иверская","феодоровская","казанская","владимирская",
    "тихвинская","смоленская","знамение","умиление","одигитрия","троеручица",
    "всех скорбящих радость","неопалимая купина","покров","елеуса","панагия",
    "аристократуса","пюхтицкая","ченстоховская","живоносный источник",
    "страстная","акафистная","боголюбская","азовская","ахтырская",
    "цареградская","скорбящая","страстная","знамение новгородское",
    "святые",  // "богоматерь святые" — убираем
];

function cleanBogomater(token) {
    if (!token.toLowerCase().startsWith("богоматерь")) return token;
    let s = token.toLowerCase();
    for (const ep of BOGOMAT_EPITHETS) {
        const re = new RegExp(`^богоматерь\\s+${ep.replace(/[.*+?^${}()|[\]\\]/g,"\\$&")}\\b.*$`, "i");
        if (re.test(s)) return "богоматерь";
    }
    // Если после "богоматерь" идёт что-то — убираем эпитет, оставляем "богоматерь"
    // Но НЕ убираем, если следует запятая (значит это отдельный человек)
    return token.replace(/^богоматерь\s+(?!и\b).+$/, "богоматерь");
}

// ─── [6] НОРМАЛИЗАЦИЯ ХРИСТА И БОГОРОДИЦЫ ───────────────────────────────────
function normalizeChristAndMother(tokens) {
    const hasBogo = tokens.some(t =>
        t.toLowerCase() === "богоматерь" || t.toLowerCase().startsWith("богоматерь ")
        || t.toLowerCase() === "богородица"
    );
    return tokens.map(t => {
        let s = t.toLowerCase();
        // "богородица" → "богоматерь"
        if (s === "богородица") return "богоматерь";
        if (s.startsWith("богородица ")) return "богоматерь" + s.slice("богородица".length);
        // Христос-эммануил, ветхий денми и пр. → "христос"
        s = s.replace(/\bхристос[\s-]отрок\b/g, "младенец");
        s = s.replace(/\bмладенец[\s-]христос\b/g, "младенец");
        s = s.replace(/\bхристос\s+эммануил\b/g, "христос");
        s = s.replace(/\bхристос\s+ветхий\s+денми\b/g, "христос");
        s = s.replace(/\bиисус\s+младенец\b/g, "младенец");
        // "исус" → "иисус"
        s = s.replace(/\bисус\b/g, "иисус");
        // Если рядом богоматерь, "христос" → "младенец"
        if (hasBogo && s === "христос") s = "младенец";
        return s;
    });
}

