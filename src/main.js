// "антоний и феодосий печерские" → каждый с нужным чином
function splitSimplePair(token) {
    // Ищем "имя1 и имя2 [суффикс]"
    const m = token.match(/^(.+?)\s+и\s+(.+?)(?:\s+(\S+(?:ские|ские)))?$/i);
    if (!m) return null;
    const name1 = m[1].trim();
    const name2 = m[2].trim();
    const suffix = m[3] || "";
    const pairKey = `${name1.toLowerCase()} и ${name2.toLowerCase()}`;
    if (STABLE_PAIRS.has(pairKey)) {
        // Устойчивая пара — оставляем как есть
        return null;
    }
    if (suffix) {
        const suf = getSingularSuffix(suffix, "");
        return [
            `${name1}${suf ? " " + suf : ""}`,
            `${name2}${suf ? " " + suf : ""}`,
        ];
    }
    return [name1, name2];
}

