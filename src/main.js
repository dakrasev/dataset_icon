function splitPairWithSuffix(token) {
    // Формат: "[чин] имя1 и имя2 [суффикс]"  или "имя1 и имя2 [суффикс]"
    const pluralChins = Object.keys(CHIN_PLURAL).join("|");

    // Паттерн: [чин_мн] имя1 и имя2 [суффикс_мн]?
    const reWithChin = new RegExp(`^(${pluralChins})\\s+(.+?)\\s+и\\s+(.+?)(?:\\s+(\\S+(?:ские|ские|ские)))?$`, "i");
    // Паттерн без чина: имя1 и имя2 [суффикс]
    const reSimple = new RegExp(`^(.+?)\\s+и\\s+(.+?)(?:\\s+(\\S+(?:ские|ские)))?$`, "i");

    let m = token.match(reWithChin);
    if (m) {
        const chinPlural = m[1].toLowerCase();
        const name1 = m[2].trim();
        const name2 = m[3].trim();
        const suffix = m[4] || "";
        const pairKey = `${name1.toLowerCase()} и ${name2.toLowerCase()}`;
        if (STABLE_PAIRS.has(pairKey)) return null;
        const chinSing = CHIN_PLURAL[chinPlural];
        const suf1 = getSingularSuffix(suffix, chinSing);
        const suf2 = suf1;
        return [
            `${chinSing} ${name1}${suf1 ? " " + suf1 : ""}`,
            `${chinSing} ${name2}${suf2 ? " " + suf2 : ""}`,
        ];
    }
    return null;
}

function getSingularSuffix(suffix, chin) {
    if (!suffix) return "";
    // "ские" → "ский" или "ская" в зависимости от рода чина
    const femaleChinSet = new Set(["мученица","великомученица","преподобная","равноапостольная","праведная","блаженная"]);
    if (suffix.endsWith("ские") || suffix.endsWith("ские")) {
        const base = suffix.slice(0, -2);
        return femaleChinSet.has(chin) ? base + "ая" : base + "ий";
    }
    return suffix;
}

