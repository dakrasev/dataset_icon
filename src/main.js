const BOGOMAT_EPITHETS = [
    "великая панагия","иверская","феодоровская","казанская","владимирская",
    "тихвинская","смоленская","знамение","умиление","одигитрия","троеручица",
    "всех скорбящих радость","неопалимая купина","покров","елеуса","панагия",
    "аристократуса","пюхтицкая","ченстоховская","живоносный источник",
    "страстная","акафистная","боголюбская","азовская","ахтырская","цареградская",
    "и святой георгий кефалофорос","феодоровская","умягчение злых сердец",
];

function cleanBogomater(token) {
    const lower = token.toLowerCase();
    if (!lower.startsWith("богоматерь")) return token;
    for (const ep of BOGOMAT_EPITHETS) {
        if (lower === "богоматерь " + ep || lower.startsWith("богоматерь " + ep + " ")) {
            return "богоматерь";
        }
    }
    // Если после "богоматерь" что-то лишнее — убираем
    if (lower !== "богоматерь" && /^богоматерь\s+\S/.test(lower)) {
        // Но только известные эпитеты — не трогаем "богоматерь святые" отдельно
        for (const ep of BOGOMAT_EPITHETS) {
            const replaced = lower.replace(new RegExp(`\\s+${ep.replace(/[.*+?^${}()|[\]\\]/g,"\\$&")}\\b`, "i"), "");
            if (replaced !== lower) return cleanJunk(replaced);
        }
    }
    return token;
}

