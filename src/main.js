// ─── ОБРАБОТКА ЯЧЕЙКИ ────────────────────────────────────────────────────────
function processCell(rawValue) {
    if (!rawValue || typeof rawValue !== "string") return rawValue;
    if (!rawValue.trim() || rawValue.trim().toLowerCase() === "nan") return "";

    // 1) В нижний регистр
    let s = rawValue.toLowerCase();
    // 2) Мягкий перенос, BOM
    s = s.replace(/\u00AD/g, "").replace(/\uFEFF/g, "");
    // 3) Раскрываем сокращения
    s = expandAbbreviations(s);

    // 4) Разбиваем по запятой и точке с запятой
    let tokens = s.split(/[,;]/).map(t => t.trim()).filter(Boolean);

    // 5) Для каждого токена: cleanJunk → fixTypos → expandAbbreviations ещё раз
    tokens = tokens.flatMap(token => {
        token = cleanJunk(token);
        if (!token) return [];
        token = expandAbbreviations(token);
        token = normalizeSv(token);
        token = fixTypos(token);

        // Если fixTypos вернул строку с запятой — разбиваем
        if (token.includes(",")) {
            return token.split(",").map(t => cleanJunk(fixTypos(expandAbbreviations(t.trim())))).filter(Boolean);
        }
        return [token];
    });

    // 6) Разбиваем "и" токены
    tokens = tokens.flatMap(t => splitAndToken(t));

    // 7) Богоматерь: убираем эпитеты
    tokens = tokens.map(cleanBogomater);

    // 8) Нормализация Христа/Богородицы
    tokens = normalizeChristAndMother(tokens);

    // 9) Финальная дедупликация + исправление опечаток ещё раз
    tokens = tokens.flatMap(t => {
        t = cleanJunk(t);
        if (!t) return [];
        t = fixTypos(t);
        if (t.includes(",")) {
            return t.split(",").map(x => cleanJunk(dedupPerson(fixTypos(x.trim())))).filter(Boolean);
        }
        t = KNOWN_FINAL[t.toLowerCase().trim()] ?? t;
        if (t.includes(",")) {
            return t.split(",").map(x => cleanJunk(dedupPerson(x.trim()))).filter(Boolean);
        }
        t = dedupPerson(t);
        return [cleanJunk(t)];
    }).filter(Boolean);

    // 10) Согласование рода
    tokens = tokens.map(fixGender);

    // 11) Убираем пустые и мусорные токены
    const JUNK_TOKENS = new Set(["", "nan", "с", "и", "в", "а", "на", "по", "от", "до"]);
    tokens = tokens.filter(t => t && !JUNK_TOKENS.has(t.toLowerCase().trim()));

    // 12) Дедупликация в рамках одной ячейки
    const seen = new Set();
    tokens = tokens.filter(t => {
        const key = t.toLowerCase().trim();
        if (seen.has(key)) return false;
        seen.add(key);
        return true;
    });

    return tokens.map(cleanJunk).filter(Boolean).join(", ");
}

// ─── CSV ПАРСЕР ──────────────────────────────────────────────────────────────
function parseCSV(text, delim) {
    const lines = [];
    let inQuote = false, cell = "", row = [];
    for (let i = 0; i < text.length; i++) {
        const ch = text[i], next = text[i + 1];
        if (inQuote) {
            if (ch === '"' && next === '"') { cell += '"'; i++; }
            else if (ch === '"') inQuote = false;
            else cell += ch;
        } else {
            if (ch === '"') { inQuote = true; }
            else if (ch === delim) { row.push(cell); cell = ""; }
            else if (ch === "\r" && next === "\n") { row.push(cell); lines.push(row); row = []; cell = ""; i++; }
            else if (ch === "\n") { row.push(cell); lines.push(row); row = []; cell = ""; }
            else cell += ch;
        }
    }
    if (cell || row.length > 0) { row.push(cell); lines.push(row); }
    if (!lines.length) return { fields: [], data: [] };
    const fields = lines[0];
    const data = lines.slice(1).map(r => {
        const obj = {};
        fields.forEach((f, i) => { obj[f] = r[i] ?? ""; });
        return obj;
    });
    return { fields, data };
}

function serializeCSV(fields, data, delim) {
    const esc = val => {
        const s = String(val ?? "");
        if (s.includes('"') || s.includes(delim) || s.includes("\n") || s.includes("\r"))
            return '"' + s.replace(/"/g, '""') + '"';
        return s;
    };
    const rows = [
        fields.map(esc).join(delim),
        ...data.map(row => fields.map(f => esc(row[f])).join(delim)),
    ];
    return rows.join("\r\n");
}

// ─── ТОЧКА ВХОДА ─────────────────────────────────────────────────────────────
function main() {
    const [,, inputPath, outputPath] = process.argv;
    if (!inputPath || !outputPath) {
        console.error("Использование: node cleanPersonalii_v2.js input.csv output.csv");
        process.exit(1);
    }
    console.log(`📂 Читаю: ${inputPath}`);
    let raw = fs.readFileSync(inputPath, "utf-8");
    if (raw.charCodeAt(0) === 0xfeff) { raw = raw.slice(1); console.log("  BOM убран"); }

    const { fields, data } = parseCSV(raw, ";");
    console.log(`  Колонки (${fields.length}): ${fields.join(" | ")}`);
    console.log(`  Строк данных: ${data.length}`);

    const COL = "Персоналии";
    if (!fields.includes(COL)) {
        console.error(` Колонка "${COL}" не найдена!`);
        process.exit(1);
    }

    let changed = 0, total = 0;
    const out = data.map(row => {
        if (!row[COL]?.trim()) return row;
        total++;
        const orig = row[COL];
        const cleaned = processCell(orig);
        if (cleaned !== orig) changed++;
        return { ...row, [COL]: cleaned };
    });

    const bom = "\uFEFF";
    fs.writeFileSync(outputPath, bom + serializeCSV(fields, out, ";"), "utf-8");
    console.log(`\n Готово!`);
    console.log(`   Обработано строк: ${total}`);
    console.log(`   Изменено: ${changed}`);
    console.log(`   Сохранено → ${outputPath}`);
}

main();