function fixGender(token) {
    const lower = token.toLowerCase();
    // Убираем "святитель мученица анна" и подобные ошибки двойного чина
    const chinNames = ["великомученица","мученица","священномученица","преподобная",
        "великомученик","мученик","священномученик","преподобный","святитель",
        "благоверный","благоверная","блаженный","блаженная","праведный","праведная"];
    for (const a of chinNames) {
        for (const b of chinNames) {
            if (a !== b && lower.startsWith(a + " " + b + " ")) {
                // Два чина подряд → берём второй (он обычно правильный)
                return token.slice(a.length + 1);
            }
        }
    }
    // Мужской чин + женское имя → женский чин
    for (const [maleChin, femaleChin] of Object.entries(GENDER_FIX_MALE)) {
        if (lower.startsWith(maleChin + " ")) {
            const rest = token.slice(maleChin.length + 1);
            const firstName = rest.split(/\s+/)[0].toLowerCase();
            if (FEMALE_NAMES.has(firstName)) return femaleChin + " " + rest;
        }
    }
    // Женский чин + мужское имя → мужской чин (обратная проверка)
    for (const [maleChin, femaleChin] of Object.entries(GENDER_FIX_MALE)) {
        if (lower.startsWith(femaleChin + " ")) {
            const rest = token.slice(femaleChin.length + 1);
            const firstName = rest.split(/\s+/)[0].toLowerCase();
            if (MALE_NAMES_CHECK.has(firstName) && !FEMALE_NAMES.has(firstName)) {
                return maleChin + " " + rest;
            }
        }
    }
    return token;
}

