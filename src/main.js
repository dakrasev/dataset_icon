function expandAbbreviations(s) {
    for (const [re, rep] of ABBREVIATIONS) s = s.replace(re, rep);
    return s.replace(/\s{2,}/g, " ").trim();
}

// ─── [2] МУСОРНЫЕ ПАТТЕРНЫ ───────────────────────────────────────────────────
// Удаляем прямо из строки до/после разбивки
function cleanJunk(s) {
    if (!s || typeof s !== "string") return "";
    s = s.replace(/\u00AD/g, "");  // мягкий перенос
    s = s.replace(/\uFEFF/g, "");  // BOM
    // Удаляем ведущий и хвостовой мусор: точки, двоеточия, скобки, тире, кавычки
    s = s.replace(/^[\s.,;:?!()\[\]{}\-–—«»"'`\u2019\u201C\u201D]+/, "");
    s = s.replace(/[\s.,;:?!()\[\]{}\-–—«»"'`\u2019\u201C\u201D]+$/, "");
    // Точки внутри (кроме сокращений — они уже раскрыты)
    s = s.replace(/\s{2,}/g, " ");
    return s.trim();
}

