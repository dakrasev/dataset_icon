function normalizeChristAndMother(tokens) {
    const hasBogo = tokens.some(t => {
        const l = t.toLowerCase();
        return l === "богоматерь" || l.startsWith("богоматерь ") || l === "богородица";
    });
    return tokens.map(t => {
        let s = t.toLowerCase();
        if (s === "богородица") return "богоматерь";
        if (s.startsWith("богородица ")) return "богоматерь" + s.slice("богородица".length);
        s = s.replace(/\bхристос[\s-]отрок\b/g, "младенец");
        s = s.replace(/\bмладенец[\s-]христос\b/g, "младенец");
        s = s.replace(/\bиисус\s+младенец\b/g, "младенец");
        s = s.replace(/\bхристос\s+эммануил\b/g, "христос");
        s = s.replace(/\bхристос\s+ветхий\s+денми\b/g, "христос");
        s = s.replace(/\bисус\b/g, "иисус");
        if (hasBogo && s === "христос") s = "младенец";
        return s;
    });
}

