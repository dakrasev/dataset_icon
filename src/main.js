function splitChinWithTwo(token) {
    const chinRe = CHIN_SINGULAR_LIST.join("|");
    const re = new RegExp(`^(${chinRe})\\s+(.+?)\\s+и\\s+(.+?)$`, "i");
    const m = token.match(re);
    if (!m) return null;
    const chin = m[1].toLowerCase();
    const name1 = m[2].trim();
    const name2 = m[3].trim();
    const pairKey = `${name1.toLowerCase()} и ${name2.toLowerCase()}`;
    const pairKeyRev = `${name2.toLowerCase()} и ${name1.toLowerCase()}`;
    if (STABLE_PAIRS.has(pairKey) || STABLE_PAIRS.has(pairKeyRev)) return null;
    return [`${chin} ${name1}`, `${chin} ${name2}`];
}

// "имя1 и имя2 чин" → "чин имя1, чин имя2"
function splitChinAtEnd(token) {
    for (const chin of CHIN_SINGULAR_LIST) {
        if (token.toLowerCase().endsWith(" " + chin)) {
            const rest = token.slice(0, -(chin.length + 1)).trim();
            const m = rest.match(/^(.+?)\s+и\s+(.+)$/);
            if (m) {
                const pairKey = `${m[1].toLowerCase()} и ${m[2].toLowerCase()}`;
                if (STABLE_PAIRS.has(pairKey)) return null;
                return [`${chin} ${m[1].trim()}`, `${chin} ${m[2].trim()}`];
            }
        }
    }
    return null;
}

function splitAndToken(token) {
    if (!token.includes(" и ")) return [token];

    // 1) Проверяем через TYPOS (может уже есть готовая замена)
    const typoResult = fixTypos(token);
    if (typoResult !== token && typoResult.includes(",")) {
        return typoResult.split(",").map(t => cleanJunk(t.trim())).filter(Boolean);
    }

    // 2) "чин X и Y" (единственное число, одиночный чин)
    const withOneChin = splitChinWithTwo(token);
    if (withOneChin) return withOneChin;

    // 3) "X и Y чин" (чин в конце)
    const chinEnd = splitChinAtEnd(token);
    if (chinEnd) return chinEnd;

    // 4) "ЧИНЫ X и Y" (множественное число)
    const withPluralChin = splitPair(token);
    if (withPluralChin) return withPluralChin;

    // 5) Проверяем устойчивые пары
    const lower = token.toLowerCase();
    for (const pair of STABLE_PAIRS) {
        if (lower === pair || lower.includes(pair)) return [token];
    }

    return [token];
}

