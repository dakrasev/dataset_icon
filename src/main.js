// Суффикс множественного числа → единственное
function singularizeSuffix(suffix, isFemaleChin) {
    if (!suffix) return "";
    // "ские" → "ский" или "ская"
    if (suffix.match(/ские$/i)) {
        const base = suffix.replace(/ские$/i, "");
        return isFemaleChin ? base + "ская" : base + "ский";
    }
    if (suffix.match(/ские$/i)) {
        const base = suffix.replace(/ские$/i, "");
        return isFemaleChin ? base + "ская" : base + "ский";
    }
    return suffix;
}

function splitPair(token) {
    const pluralRe = Object.keys(CHIN_PLURAL).join("|");
    // Паттерн: ЧИН_МН имя1 [и суффикс]? и имя2 [суффикс]?
    const re = new RegExp(`^(${pluralRe})\\s+(.+?)\\s+и\\s+(.+?)(?:\\s+(\\S+(?:ские|ские)))?$`, "i");
    const m = token.match(re);
    if (!m) return null;

    const chinPlural = m[1].toLowerCase();
    const name1 = m[2].trim();
    const name2raw = m[3].trim();
    // суффикс может быть в name2 или отдельно
    let name2 = name2raw;
    let suffix = m[4] || "";

    // Проверяем устойчивую пару
    const pairKey = `${name1.toLowerCase()} и ${name2.toLowerCase()}`;
    const pairKeyRev = `${name2.toLowerCase()} и ${name1.toLowerCase()}`;
    if (STABLE_PAIRS.has(pairKey) || STABLE_PAIRS.has(pairKeyRev)) return null;

    const chinSing = CHIN_PLURAL[chinPlural];
    const isF = ["мученица","великомученица","преподобная","равноапостольная",
        "праведная","блаженная","преподобномученица"].includes(chinSing);

    const suf = singularizeSuffix(suffix, isF);

    return [
        `${chinSing} ${name1}${suf ? " " + suf : ""}`,
        `${chinSing} ${name2}${suf ? " " + suf : ""}`,
    ];
}

